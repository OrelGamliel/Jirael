# ── styled-components theme type augmentation ─────────────────────────────────
mkdir -p src/types

cat > src/types/styled.d.ts << 'EOF'
import { AppTheme } from '../providers/theme';

declare module 'styled-components' {
  export interface DefaultTheme extends AppTheme {}
}
EOF

# ── common ───────────────────────────────────────────────────────────────────
mkdir -p src/common/components
mkdir -p src/common/hooks
mkdir -p src/common/constants

touch src/common/components/.gitkeep
touch src/common/hooks/.gitkeep
touch src/common/constants/index.ts

# ── hooks (app-level) ────────────────────────────────────────────────────────
mkdir -p src/hooks
touch src/hooks/.gitkeep

# ── utils (app-level) ────────────────────────────────────────────────────────
mkdir -p src/utils
touch src/utils/.gitkeep

# ── features ─────────────────────────────────────────────────────────────────
# Creates a scaffold for one example feature. Duplicate this pattern per feature.
FEATURE="example"
mkdir -p src/features/$FEATURE/{hooks,components,widgets,state,types,utils,constants}

cat > src/features/$FEATURE/hooks/use${FEATURE^}.ts << 'EOF'
// Discriminated union state — all hooks must follow this pattern
type ExampleState =
  | { status: 'loading' }
  | { status: 'error'; error: Error }
  | { status: 'success'; data: unknown[] };

export function useExample(): ExampleState {
  // call RTK Query here, map to discriminated union
  return { status: 'loading' };
}
EOF

cat > src/features/$FEATURE/widgets/ExampleWidget.tsx << 'EOF'
import styled from 'styled-components';
import { useExample } from '../hooks/useExample';

const StyledWrapper = styled.div`
  padding: ${({ theme }) => theme.spacing.md};
  border-radius: ${({ theme }) => theme.borderRadius.md};
  background: ${({ theme }) => theme.colors.surface};
`;

// Pure UI — Storybook targets this export
export function ExampleWidgetContent(props: ReturnType<typeof useExample>) {
  if (props.status === 'loading') return <StyledWrapper>Loading…</StyledWrapper>;
  if (props.status === 'error') return <StyledWrapper>Error: {props.error.message}</StyledWrapper>;
  return <StyledWrapper>Data: {JSON.stringify(props.data)}</StyledWrapper>;
}

// Container — calls hook, passes state down. Nothing else.
export function ExampleWidget() {
  const state = useExample();
  return <ExampleWidgetContent {...state} />;
}
EOF

cat > src/features/$FEATURE/types/types.ts << 'EOF'
import { z } from 'zod';

export const ExampleSchema = z.object({
  id: z.string(),
});

export type Example = z.infer<typeof ExampleSchema>;
EOF

cat > src/features/$FEATURE/index.ts << 'EOF'
// Public surface of the example feature
export { ExampleWidget, ExampleWidgetContent } from './widgets/ExampleWidget';
EOF

# ── main entry ───────────────────────────────────────────────────────────────
cat > src/main.tsx << 'EOF'
import React from 'react';
import ReactDOM from 'react-dom/client';
import { AppProviders } from './providers/AppProviders';

ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <AppProviders />
  </React.StrictMode>
);
EOF

echo ""
echo "✅ Done! Structure created:"
echo ""
find src -type f | sort
