#!/usr/bin/env bash
# scaffold.sh — run once at project root after `npm create vite@latest . -- --template react-ts`
set -e

echo "📦 Installing dependencies..."

npm install \
  @reduxjs/toolkit \
  react-redux \
  @tanstack/react-router \
  @tanstack/router-devtools \
  zod \
  @sentry/react \
  styled-components

npm install -D \
  @tanstack/router-plugin \
  @tanstack/router-cli \
  @types/react \
  @types/react-dom \
  @types/styled-components \
  vite-tsconfig-paths

echo "🗂  Creating folder structure..."

# ── app ────────────────────────────────────────────────────────────────────
mkdir -p src/app/api

cat > src/app/store.ts << 'EOF'
import { configureStore } from '@reduxjs/toolkit';
import { baseApi } from './api/baseApi';

export const store = configureStore({
  reducer: {
    [baseApi.reducerPath]: baseApi.reducer,
    // feature slices go here
  },
  middleware: (getDefaultMiddleware) =>
    getDefaultMiddleware().concat(baseApi.middleware),
});

export type RootState = ReturnType<typeof store.getState>;
export type AppDispatch = typeof store.dispatch;
EOF

cat > src/app/api/baseApi.ts << 'EOF'
import { createApi, fetchBaseQuery } from '@reduxjs/toolkit/query/react';

export const baseApi = createApi({
  reducerPath: 'api',
  baseQuery: fetchBaseQuery({ baseUrl: '/api' }),
  endpoints: () => ({}),
});
EOF

cat > src/app/router.tsx << 'EOF'
import { createRouter } from '@tanstack/react-router';
import { routeTree } from '../routes/routeTree.gen'; // generated by TanStack Router CLI

export const router = createRouter({ routeTree });

declare module '@tanstack/react-router' {
  interface Register {
    router: typeof router;
  }
}
EOF

# ── routes ──────────────────────────────────────────────────────────────────
mkdir -p src/routes

cat > src/routes/__root.tsx << 'EOF'
import { createRootRoute, Outlet } from '@tanstack/react-router';

export const Route = createRootRoute({
  component: () => <Outlet />,
});
EOF

cat > src/routes/index.tsx << 'EOF'
import { createFileRoute } from '@tanstack/react-router';

export const Route = createFileRoute('/')({
  component: () => <div>Home</div>,
});
EOF

# ── providers ───────────────────────────────────────────────────────────────
mkdir -p src/providers

cat > src/providers/AppProviders.tsx << 'EOF'
import { Provider } from 'react-redux';
import { RouterProvider } from '@tanstack/react-router';
import { ThemeProvider } from 'styled-components';
import { store } from '../app/store';
import { router } from '../app/router';
import { theme } from './theme';
import { GlobalStyles } from './GlobalStyles';

export function AppProviders() {
  return (
    <Provider store={store}>
      <ThemeProvider theme={theme}>
        <GlobalStyles />
        <RouterProvider router={router} />
      </ThemeProvider>
    </Provider>
  );
}
EOF

cat > src/providers/theme.ts << 'EOF'
